//    Parameter delta : [ 0,  (+inf) [
  if ( (!((delta >=0))) )
  {
    UserWarning(*this) << "ADMSmvs_2_0_0_etsoi: Parameter delta value " << delta << " out of range [ 0,  (+inf) [";
  }

