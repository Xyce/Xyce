// Analog Function limRTH
template<typename ScalarT> ScalarT limRTH(ScalarT orig, ScalarT old)
{


  ScalarT limRTH;
  ScalarT t0;
  ScalarT t1;
  ScalarT retval;
  {
    t0 = (orig-old);
    t1 = fabs(t0);
    retval = orig;
    if ((t1>5.0))
    {
      if ((t0>0))
      {
        retval = (old+5.0);
      }
      else
      {
        retval = (old-5.0);
      }
    }
    limRTH = retval;
  }
  return(limRTH);
}

