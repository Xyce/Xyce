      // Analog Function lexp
template<typename ScalarT> ScalarT lexp(ScalarT x)
{


    ScalarT lexp;
{
if ((x>80.0))
{
lexp = (5.540622384e+34*((1.0+x)-80.0));
}
else
{
if ((x<(-80.0)))
{
lexp = 1.804851387e-35;
}
else
{
lexp = exp(x);
}
}
}
return(lexp);
}
